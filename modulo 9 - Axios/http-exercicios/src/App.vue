<template>
	<div id="app" class="container">
		<h1>HTTP com Axios</h1>
		<b-card>

			<b-form-group label="Nome:">
				<b-form-input type="text" size="lg" 
					v-model="usuario.nome" 
					placeholder="Informe o Nome">
				</b-form-input> 
			</b-form-group>
			<b-form-group label="E-mail:">
				<b-form-input type="text" size="lg" 
					v-model="usuario.email" 
					placeholder="Informe o E-mail"
					@keydown.native.enter="salvar">
				</b-form-input> 
			</b-form-group>
			<hr>
			<b-button @click="salvar" size="lg" variant="primary" class="mr-1">Salvar</b-button>
			<b-button @click="ObterUsuarios" size="lg" variant="success">Obter Usu√°rios</b-button>

		</b-card>
		<hr>

		<b-list-group>
			<b-list-group-item v-for="(usu, id) in usuarios" :key="id" class="mb-1">
				<strong>Id: </strong> {{ id }} <br>
				<strong>Nome: </strong> {{ usu.nome }} <br>
				<strong>E-mail: </strong> {{ usu.email }} <br>
			</b-list-group-item>
		</b-list-group>

	</div>
</template>

<script>
export default {
	data(){
		return{
			usuarios:[],
			usuario:{
				nome:'',
				email:''
			}
		}
	},
	methods:{
		salvar(){
			alert();
			this.$http.post('usuarios.json', this.usuario)
				.then(resp => {
					this.usuario.nome = ''
					this.usuario.email = ''
			})
		},
		ObterUsuarios(){
			this.$http.get('usuarios.json').then(res =>{
				this.usuarios = res.data
			})
		}
	}

}
</script>

